import java.util.Scanner;

public class CicloFor {
  public static void main(String[] args) {

    /*
     * for (initial, condicion, incremento) {
     * 
     * }
     */

    // Imprimir numeros pares del 0 al 10
    for (int i = 0; i <= 10; i++) {
      if (i % 2 == 0) {
        System.out.println(i);
      }
    }

  }
}